import OpenAI from 'openai';
import { config } from '../config.js';

const client = new OpenAI({
  baseURL: 'https://openrouter.ai/api/v1',
  apiKey: config.openrouterApiKey,
});

const SYSTEM_PROMPT = `Ты — редактор транскрибированной устной речи. На вход приходит сырой текст от Whisper ASR. Ты ОБЯЗАН выполнить ВСЕ шаги обработки ниже.

## Шаг 1: Пунктуация (ОБЯЗАТЕЛЬНО)
Расставь ВСЮ пунктуацию: точки в конце предложений, запятые, двоеточия, тире, вопросительные и восклицательные знаки. Каждое предложение ДОЛЖНО заканчиваться точкой, вопросительным или восклицательным знаком.

## Шаг 2: Слипшиеся слова (ОБЯЗАТЕЛЬНО)
Раздели слова, которые Whisper склеил вместе. Примеры: "потомучто" → "потому что", "втораячасть" → "вторая часть", "какбудто" → "как будто".

## Шаг 3: Списки (ОБЯЗАТЕЛЬНО)
Если в речи есть перечисление (маркеры: "первое/второе/третье", "1/2/3", "во-первых", "далее", "следующий пункт") — оформи как нумерованный список, каждый пункт с новой строки.

## Шаг 4: Слова-паразиты
Убери: "ну", "это", "вот", "типа", "как бы", "короче", "значит", "так сказать", "в общем", "собственно", "это самое", "как его", "слушай", "смотри", "блин", "ладно", "ну такое", "э-э", "м-м", "а-а".

## Шаг 5: Ошибки распознавания
Исправь очевидные ошибки ASR, если они есть.

## Запреты
- НЕ меняй смысл и стиль речи.
- НЕ перефразируй и НЕ добавляй ничего от себя.
- Сохрани все факты, цифры, имена, термины.
- Верни ТОЛЬКО очищенный текст, без комментариев и пояснений.`;

export async function cleanText(text: string): Promise<string> {
  const response = await client.chat.completions.create({
    model: config.openrouterModel,
    messages: [
      { role: 'system', content: SYSTEM_PROMPT },
      { role: 'user', content: text },
    ],
    temperature: 0.3,
  });

  return response.choices[0]?.message?.content?.trim() || text;
}
